#!/usr/bin/env bash
# =============================================================================
# Temper — Claude Code on the Web Environment Setup
# =============================================================================
#
# Lightweight SessionStart hook that runs on every session start/resume.
# Must complete in seconds, not minutes.
#
# This script ONLY handles:
#   - PATH configuration
#   - Environment variables (persisted to CLAUDE_ENV_FILE)
#   - Git hooks
#   - .env file generation
#
# Heavy installations (cargo tools, PostgreSQL, etc.) are handled by
# individual scripts in tools/cargo-make/scripts/claude-web/ and documented
# in CLAUDE.md. The web agent can run them on-demand when specific
# capabilities are needed.
#
# Usage:
#   ./tools/bin/setup-claude-web.sh           # Auto-invoked by SessionStart hook
#   FORCE_SETUP=1 ./tools/bin/setup-claude-web.sh  # Run even outside remote env
#
# For full environment setup (tools + services), run:
#   ./tools/bin/setup-claude-web-full.sh
#
# =============================================================================

set -euo pipefail

# ---------------------------------------------------------------------------
# Environment Guard
# ---------------------------------------------------------------------------
if [ "${CLAUDE_CODE_REMOTE:-}" != "true" ] && [ "${FORCE_SETUP:-}" != "1" ]; then
  exit 0
fi

# ---------------------------------------------------------------------------
# Bootstrap
# ---------------------------------------------------------------------------
PROJECT_DIR="${CLAUDE_PROJECT_DIR:-$(cd "$(dirname "${BASH_SOURCE[0]}")/../.." && pwd)}"
LIB_DIR="${PROJECT_DIR}/tools/cargo-make/scripts/claude-web"

# Ensure common tool directories are in PATH from the start
export PATH="$HOME/.local/bin:$HOME/.cargo/bin:$PATH"

# Source the shared helpers
source "${LIB_DIR}/setup-common.sh"

echo ""
echo "==> Configuring temper environment for Claude Code on the web"
echo "  Project: $PROJECT_DIR"

# ---------------------------------------------------------------------------
# Phase 1: PATH and environment variables (instant, no I/O)
# ---------------------------------------------------------------------------
persist_env 'export PATH="$HOME/.local/bin:$HOME/.cargo/bin:$PATH"'

# Core project paths
persist_env "export WORKSPACE_PATH=\"$PROJECT_DIR\""

# Rust
persist_env 'export RUST_LOG="warn"'
persist_env 'export LOG_LEVEL="warn"'

# Database — default to Docker port 5437, but native fallback may use 5432
persist_env 'export DATABASE_URL="postgresql://temper:temper@localhost:5437/temper_development"'
persist_env 'export TEST_DATABASE_URL="postgresql://temper:temper@localhost:5437/temper_test"'

# Use the committed `.sqlx/` offline cache so `cargo check / clippy` compile
# without a live database. Tests and the real build path still use the live
# DB when available (setup-claude-web-full.sh brings up Postgres).
persist_env 'export SQLX_OFFLINE="true"'

# Auth (test/development values — real values come from Vercel env)
persist_env 'export JWKS_URL="https://test-jwks-url/.well-known/jwks.json"'
persist_env 'export AUTH_ISSUER="temper-test"'
persist_env 'export AUTH_AUDIENCE="temper-api-test"'
persist_env 'export AUTH_PROVIDER_NAME="neon_auth"'

# CORS
persist_env 'export CORS_ORIGINS="http://localhost:3000"'

# API
persist_env 'export PORT="3000"'
persist_env 'export ENABLE_SWAGGER="true"'

log_ok "environment variables persisted to session"

# ---------------------------------------------------------------------------
# Phase 2: Git hooks (fast, local filesystem only)
# ---------------------------------------------------------------------------
if git rev-parse --is-inside-work-tree &>/dev/null; then
  git config core.hooksPath githooks
  chmod +x "$PROJECT_DIR/githooks/"* 2>/dev/null || true
  log_ok "git hooks configured (githooks/pre-commit)"
fi

# ---------------------------------------------------------------------------
# Phase 3: Generate .env file
# ---------------------------------------------------------------------------
generate_env() {
  cat > "$PROJECT_DIR/.env" << ENVEOF
# Generated by setup-claude-web.sh
RUST_LOG=warn
LOG_LEVEL=warn
DATABASE_URL=postgresql://temper:temper@localhost:5437/temper_development
JWKS_URL=https://test-jwks-url/.well-known/jwks.json
AUTH_ISSUER=temper-test
AUTH_AUDIENCE=temper-api-test
AUTH_PROVIDER_NAME=neon_auth
CORS_ORIGINS=http://localhost:3000
PORT=3000
ENABLE_SWAGGER=true
ENVEOF
  log_ok ".env file created"
}

generate_env

# ---------------------------------------------------------------------------
# Phase 3a: git-lfs (required by pre-push hook + onnxruntime binary assets)
# ---------------------------------------------------------------------------
# The repo's `githooks/pre-push` invokes `git lfs pre-push`, so git-lfs must
# be installed for any session that intends to push. Separately,
# onnxruntime-node ships model binaries through LFS — another hard requirement.
# Cheap to install (~2s apt), safe to skip silently if already present.
ensure_git_lfs() {
  if command_exists git-lfs; then
    log_ok "git-lfs already installed ($(git-lfs version 2>/dev/null | head -1))"
    return
  fi
  if command_exists apt-get; then
    if sudo apt-get install -y -qq git-lfs >/dev/null 2>&1; then
      git lfs install --skip-repo >/dev/null 2>&1 || true
      log_ok "git-lfs installed via apt"
    else
      log_warn "git-lfs install failed — pushes will fail until resolved"
    fi
  else
    log_warn "git-lfs not installed and no apt-get — install manually to enable pushes"
  fi
}

ensure_git_lfs

# ---------------------------------------------------------------------------
# Phase 3b: SvelteKit UI env stubs + node_modules
# ---------------------------------------------------------------------------
# The SvelteKit app imports env vars from `$env/static/private` at module
# load time, so `bun run check` / `build` fail unless these names resolve
# to *something* — even a stub. Real credentials for Auth0, Neon, and the
# session cookie come from the Vercel project env in production and a
# locally-edited packages/temper-ui/.env in dev (gitignored).
generate_ui_env() {
  local ui_env="$PROJECT_DIR/packages/temper-ui/.env"
  if [ -f "$ui_env" ]; then
    log_ok "packages/temper-ui/.env already present (leaving intact)"
    return
  fi
  cat > "$ui_env" <<ENVEOF
# Generated by setup-claude-web.sh — cloud stubs only.
# Real values go in your local .env (gitignored). Never commit secrets.
API_BASE_URL=
APP_URL=http://localhost:5173
DATABASE_URL=postgresql://temper:temper@localhost:5437/temper_development
AUTH0_DOMAIN=temperkb.us.auth0.com
AUTH0_CLIENT_ID=stub-for-typecheck
AUTH0_CLIENT_SECRET=stub-for-typecheck
AUTH0_AUDIENCE=https://temperkb.io/api
SESSION_SECRET=stub-for-typecheck-stub-for-typecheck
ENVEOF
  log_ok "packages/temper-ui/.env created (stubs)"
}

generate_ui_env

# Populate node_modules so `bun run check / test / build` work.
# `bun install` is a no-op when the lockfile and node_modules are in sync.
if command_exists bun; then
  (cd "$PROJECT_DIR" && bun install --silent) \
    && log_ok "bun install complete" \
    || log_warn "bun install failed (non-fatal — check packages/temper-ui manually)"
else
  log_warn "bun not installed — skipping node_modules (install via setup-claude-web-full.sh)"
fi

# ---------------------------------------------------------------------------
# Phase 3c: temper CLI (so the agent can dogfood the product it's editing)
# ---------------------------------------------------------------------------
# Uses the one-liner release installer from README.md, which drops a
# binary symlink at ~/.local/bin/temper. Skipped if `temper` is already on
# PATH or present at a standard install location — e.g. a pre-populated
# image, a local build (~/.cargo/bin/temper from `cargo install --path`),
# or a previous run of this script.
#
# Cloud sessions don't yet have credentials to hit temper-cloud — the
# JWT-for-agents work is tracked separately — so the CLI works for
# `--help`, `--version`, `init`, and local-vault commands, but NOT
# `sync`, `push`, etc. Still valuable for reading the help surface,
# running doctor, and testing changes to the CLI itself.
# Known-good fallback version, used when the installer's "latest" GitHub
# API call gets rate-limited (common in cloud sandboxes — returns 403).
# Bump this when you cut a new release; $TEMPER_CLI_VERSION env var wins.
TEMPER_CLI_FALLBACK_VERSION="v0.1.2"

install_temper_cli() {
  if command_exists temper; then
    log_ok "temper CLI already on PATH ($(command -v temper))"
    return
  fi
  for candidate in "$HOME/.local/bin/temper" "$HOME/.cargo/bin/temper"; do
    if [ -x "$candidate" ]; then
      log_ok "temper CLI found at $candidate (not on PATH — add its dir to PATH)"
      return
    fi
  done
  if ! command_exists curl; then
    log_warn "temper CLI not installed and curl unavailable — skipping"
    return
  fi

  local installer="https://raw.githubusercontent.com/tasker-systems/temper/main/scripts/install/install.sh"
  local pin="${TEMPER_CLI_VERSION:-}"
  local log="/tmp/temper-install.log"

  # If a pin is set, go straight there. Otherwise try "latest" (one GitHub API
  # call inside the installer); if that 403s from rate-limiting, retry with
  # the hardcoded fallback version which goes straight to the release download.
  if [ -n "$pin" ]; then
    if curl -fsSL "$installer" | sh -s -- --version "$pin" >"$log" 2>&1; then
      log_ok "temper CLI $pin installed to \$HOME/.local/bin/temper"
      return
    fi
  else
    if curl -fsSL "$installer" | sh >"$log" 2>&1; then
      log_ok "temper CLI (latest) installed to \$HOME/.local/bin/temper"
      return
    fi
    if curl -fsSL "$installer" | sh -s -- --version "$TEMPER_CLI_FALLBACK_VERSION" >"$log" 2>&1; then
      log_ok "temper CLI $TEMPER_CLI_FALLBACK_VERSION installed (GitHub API unavailable, used pinned fallback)"
      return
    fi
  fi

  log_warn "temper CLI install failed (non-fatal) — see $log for details"
}

install_temper_cli

# ---------------------------------------------------------------------------
# Phase 4: Detect available tools and services
# ---------------------------------------------------------------------------
log_section "Environment status"

echo ""
echo "  Tools:"
command_exists cargo      && echo "    cargo:         $(cargo --version 2>/dev/null | awk '{print $2}' || echo 'yes')" || echo "    cargo:         NOT INSTALLED"
command_exists cargo-make && echo "    cargo-make:    yes" || echo "    cargo-make:    NOT INSTALLED"
command_exists sqlx       && echo "    sqlx-cli:      yes" || echo "    sqlx-cli:      NOT INSTALLED"
command_exists cargo-nextest && echo "    cargo-nextest: yes" || echo "    cargo-nextest: NOT INSTALLED"
command_exists gh         && echo "    gh:            $(gh --version 2>/dev/null | head -1 | awk '{print $3}' || echo 'yes')" || echo "    gh:            NOT INSTALLED"
echo ""

echo "  Services:"
if pg_isready -h localhost -p 5437 -q 2>/dev/null; then
  echo "    PostgreSQL:    ready (port 5437)"
elif pg_isready -q 2>/dev/null; then
  echo "    PostgreSQL:    ready (native, port 5432)"
else
  echo "    PostgreSQL:    NOT RUNNING"
fi
echo ""

echo "  To install missing tools or start services, run:"
echo "    ./tools/bin/setup-claude-web-full.sh"
echo ""
